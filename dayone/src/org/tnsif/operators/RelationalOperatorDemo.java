//Program for Relational-operator
package org.tnsif.operators;

public class RelationalOperatorDemo {

	public static void main(String[] args) {
		System.out.println(4==5);
		System.out.println(4!=5);
		System.out.println(4>5);
		System.out.println(4<5);
		System.out.println(4<=4);
		
	}

}
